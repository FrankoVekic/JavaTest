package exercise8;

public class County {

	private String name;
	private String prefect;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPrefect() {
		return prefect;
	}
	public void setPrefect(String prefect) {
		this.prefect = prefect;
	}
}
